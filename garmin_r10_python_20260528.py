R10_ADDRESS = "E4:7B:DD:3F:78:D7"  # Your specific device MAC

# Device Interface Service (Control/Keep-Alive)
SERVICE_UUID = "6a4e2800-667b-11e3-949a-0800200c9a66"
DEVICE_INTERFACE_NOTIFIER = "6a4e2812-667b-11e3-949a-0800200c9a66"
DEVICE_INTERFACE_WRITER = "6a4e2822-667b-11e3-949a-0800200c9a66"

# Measurement Service (Shot Data)
MEASUREMENT_SERVICE_UUID = "6a4e3400-667b-11e3-949a-0800200c9a66"
MEASUREMENT_CHAR_UUID = "6a4e3401-667b-11e3-949a-0800200c9a66" # Shot Data (Protobuf)
CONTROL_POINT_CHAR_UUID = "6a4e3402-667b-11e3-949a-0800200c9a66" # Start/Stop Radar
STATUS_CHAR_UUID = "6a4e3403-667b-11e3-949a-0800200c9a66"

# ==========================================
# DATENBANK LOGIK
# ==========================================
class ShotDatabase:
    def __init__(self, db_name="golf_data.db"):
        self.db_name = db_name
        self._init_db()

    def _init_db(self):
        with sqlite3.connect(self.db_name) as conn:
            conn.execute("""
                CREATE TABLE IF NOT EXISTS shots (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    timestamp DATETIME,
                    ball_speed REAL,
                    launch_angle REAL,
                    backspin REAL,
                    carry_dist REAL,
                    total_dist REAL,
                    club TEXT
                )
            """)

    def save_shot(self, m, club):
        with sqlite3.connect(self.db_name) as conn:
            conn.execute("""
                INSERT INTO shots (timestamp, ball_speed, launch_angle, backspin, carry_dist, total_dist, club)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """, (datetime.now(), m.get('ball_speed'), m.get('launch_angle'), 
                  m.get('backspin'), m.get('carry'), m.get('total'), club))

