# BLUETOOTH WORKER
# ==========================================
class R10Worker(QObject):
    data_received = pyqtSignal(dict)
    status_changed = pyqtSignal(str)

    def run(self):
        asyncio.run(self.main_loop())

    async def main_loop(self):
        self.status_changed.emit("connecting")
        try:
            # Auto-detect if address isn't found immediately, or use direct connect
            device = await BleakScanner.find_device_by_address(R10_ADDRESS, timeout=5.0)
            if not device:
                 # Fallback: Try to find by name if MAC fails (useful if BT cache is old)
                self.status_changed.emit("error:Device not found")
                return

            async with BleakClient(device) as client:
                self.status_changed.emit("connected")
                print(f"Connected to {client.address}")

                # 1. Enable Notifications for Shot Data
                def notification_handler(sender, data):
                    # Sender UUID helps us identify what kind of data came in
                    if sender.uuid == MEASUREMENT_CHAR_UUID:
                        self.process_shot_data(data)
                    elif sender.uuid == STATUS_CHAR_UUID:
                        print(f"Device Status: {data.hex()}")
                    elif sender.uuid == DEVICE_INTERFACE_NOTIFIER:
                        print(f"Interface Notify: {data.hex()}")

                # Subscribe to the important characteristics
                await client.start_notify(MEASUREMENT_CHAR_UUID, notification_handler)
                await client.start_notify(STATUS_CHAR_UUID, notification_handler)
                # await client.start_notify(DEVICE_INTERFACE_NOTIFIER, notification_handler)

                # 2. Initialize Device (Handshake)
                # Write 0x01 to Interface Writer (Keep-alive/Init)
                await client.write_gatt_char(DEVICE_INTERFACE_WRITER, bytearray([0x01]))
                
                # 3. Start Radar (Arm the device)
                # Write 0x01 to Control Point to start looking for balls
                await client.write_gatt_char(CONTROL_POINT_CHAR_UUID, bytearray([0x01]))
                print("R10 Radar Armed.")

                # Keep-alive Loop
                while True:
                    await asyncio.sleep(15)
                    if client.is_connected:
                        # Send Keep-Alive (standard for R10)
                        await client.write_gatt_char(DEVICE_INTERFACE_WRITER, bytearray([0x01]))
                    else:
                        break

        except Exception as e:
            print(f"Error: {e}")
            self.status_changed.emit(f"error:{str(e)}")

    def process_shot_data(self, data):
        try:
            # Parse Protobuf
            shot = LaunchMonitor_pb2.LaunchMonitor()
            shot.ParseFromString(data)
            
            # Extract fields. Note: Use .get or hasattr checks if fields are optional
            # Mapping Proto fields to our Dict
            parsed = {
                'ball_speed': round(shot.ball_speed, 1),
                'launch_angle': round(shot.launch_angle, 1),
                'backspin': int(shot.total_spin), # Proto field is total_spin
                'carry': round(shot.carry_distance, 1),
                'total': round(shot.total_distance, 1)
            }
            
            # Filter out zeroed data (sometimes R10 sends empty packets)
            if parsed['ball_speed'] > 0:
                self.data_received.emit(parsed)
                
        except Exception as e:
            print(f"Protobuf Parse Error: {e}")

